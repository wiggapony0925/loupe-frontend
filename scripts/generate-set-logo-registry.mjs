#!/usr/bin/env node
/**
 * generate-set-logo-registry.mjs
 *
 * Reads `assets/sets/_index.json` (written by fetch-set-logos.mjs) and
 * emits a TypeScript registry that Metro can statically resolve.
 *
 * Output: src/shared/setLogos.generated.ts
 *
 * The registry exposes:
 *   - POKEMON_LOGOS:  Record<setId, ImageSourcePropType>   (require'd PNGs)
 *   - POKEMON_SYMBOLS: Record<setId, ImageSourcePropType>  (require'd PNGs)
 *   - MAGIC_SYMBOLS:  Record<setCode, string>              (remote SVG URLs)
 *   - POKEMON_META / MAGIC_META / YUGIOH_META              (name + releaseDate)
 *
 * Re-run whenever you re-pull assets.
 */
import { readFile, writeFile } from "node:fs/promises";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const MANIFEST = join(ROOT, "assets", "sets", "_index.json");
const OUT = join(ROOT, "src", "shared", "setLogos.generated.ts");

const raw = await readFile(MANIFEST, "utf8");
const manifest = JSON.parse(raw);

const lines = [];
lines.push("/* eslint-disable */");
lines.push("// AUTO-GENERATED by scripts/generate-set-logo-registry.mjs");
lines.push(`// Re-run after \`node scripts/fetch-set-logos.mjs\`. Do not edit by hand.`);
lines.push(`// Fetched: ${manifest.fetchedAt}`);
lines.push("");
lines.push('import type { ImageSourcePropType } from "react-native";');
lines.push("");

// ── Pokémon ────────────────────────────────────────────────────────────────
lines.push("export const POKEMON_LOGOS: Record<string, ImageSourcePropType> = {");
for (const s of manifest.pokemon) {
  if (s.files?.logo) {
    lines.push(`  ${JSON.stringify(s.id)}: require("../../assets/sets/pokemon/${s.id}.logo.png"),`);
  }
}
lines.push("};");
lines.push("");

lines.push("export const POKEMON_SYMBOLS: Record<string, ImageSourcePropType> = {");
for (const s of manifest.pokemon) {
  if (s.files?.symbol) {
    lines.push(`  ${JSON.stringify(s.id)}: require("../../assets/sets/pokemon/${s.id}.symbol.png"),`);
  }
}
lines.push("};");
lines.push("");

// ── Magic ─────────────────────────────────────────────────────────────────
// SVGs are kept as remote URLs — `react-native-svg`'s SvgUri renders them
// without needing a Metro transformer. Scryfall URIs are stable.
lines.push("export const MAGIC_SYMBOLS: Record<string, string> = {");
for (const s of manifest.magic) {
  if (s.files?.symbol) {
    // The fetcher saved the file locally but we also need the original URL
    // for SvgUri. The manifest doesn't currently include it — reconstruct
    // from Scryfall's known pattern. Cleaner option: extend the manifest.
    // For now, point at the well-known CDN path:
    //   https://svgs.scryfall.io/sets/<code>.svg
    lines.push(`  ${JSON.stringify(s.code)}: "https://svgs.scryfall.io/sets/${s.code}.svg",`);
  }
}
lines.push("};");
lines.push("");

// ── Metadata ──────────────────────────────────────────────────────────────
lines.push("export interface SetMeta { id: string; name: string; releaseDate?: string; series?: string; }");
lines.push("");

lines.push("export const POKEMON_META: Record<string, SetMeta> = {");
for (const s of manifest.pokemon) {
  lines.push(
    `  ${JSON.stringify(s.id)}: { id: ${JSON.stringify(s.id)}, name: ${JSON.stringify(s.name)}, releaseDate: ${JSON.stringify(s.releaseDate ?? "")}, series: ${JSON.stringify(s.series ?? "")} },`,
  );
}
lines.push("};");
lines.push("");

lines.push("export const MAGIC_META: Record<string, SetMeta> = {");
for (const s of manifest.magic) {
  lines.push(
    `  ${JSON.stringify(s.code)}: { id: ${JSON.stringify(s.code)}, name: ${JSON.stringify(s.name)}, releaseDate: ${JSON.stringify(s.releaseDate ?? "")} },`,
  );
}
lines.push("};");
lines.push("");

// Yu-Gi-Oh has no logos but we still want set-name normalization to be
// possible. Just emit the set codes → names map. YGOPRODeck returns
// duplicate codes for reprints/regional variants — keep first occurrence.
lines.push("export const YUGIOH_META: Record<string, SetMeta> = {");
const seenYgo = new Set();
for (const s of manifest.yugioh) {
  if (!s.code || seenYgo.has(s.code)) continue;
  seenYgo.add(s.code);
  lines.push(
    `  ${JSON.stringify(s.code)}: { id: ${JSON.stringify(s.code)}, name: ${JSON.stringify(s.name)}, releaseDate: ${JSON.stringify(s.releaseDate ?? "")} },`,
  );
}
lines.push("};");
lines.push("");

await writeFile(OUT, lines.join("\n"));
console.log(`wrote ${OUT}`);
console.log(
  `  pokemon: ${Object.keys(manifest.pokemon).length} sets / ${manifest.pokemon.filter((s) => s.files?.logo).length} logos`,
);
console.log(
  `  magic:   ${Object.keys(manifest.magic).length} sets / ${manifest.magic.filter((s) => s.files?.symbol).length} symbols`,
);
console.log(`  yugioh:  ${manifest.yugioh.length} sets (metadata only)`);
